package edu.marcio.exemplos;

public class Loop {

    public static void main(String[] args) {

        int[] numeros = { 1, 2, 3, 4, 5 };

        // Loop for tradicional
        for (int i = 0; i < numeros.length; i++) {
            System.out.println(numeros[i]);
        }

    }
}
